  let tests =
    let set_sigil =
      if String.starts_with ~prefix:"i" width
      then String.sub width 1 (String.length width - 1)
      else width
    in
    List.map
      (fun { container; create; test_setters } ->
        one_test_template
          ~test_setters
          ~index:tested_index
          ~boxed_data
          ~tested_data
