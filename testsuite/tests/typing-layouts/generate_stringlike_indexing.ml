let indent spaces string =
  let indent_string = String.make spaces ' ' in
  String.split_on_char '\n' string
  |> List.concat_map (fun line ->
    if String.equal line "" then [ "\n" ] else [ indent_string; line; "\n" ])
  |> String.concat ""

