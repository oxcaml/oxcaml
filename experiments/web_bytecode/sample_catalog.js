export const allSamples = [...supportedSamples, ...unsupportedSamples];
export const visibleSamples = supportedSamples.filter(
  (sample) => sample.browser_status === "supported",
);

const sampleMap = new Map(allSamples.map((sample) => [sample.id, sample]));

export function getSampleById(sampleId) {
  return sampleMap.get(sampleId) ?? null;
}

export function getVisibleSamplesByTopic() {
  return topicOrder
    .map((topic) => ({
      topic,
      samples: visibleSamples.filter((sample) => sample.topic === topic),
    }))
    .filter((group) => group.samples.length > 0);
}

export function matchesExpectedText(actual, expectedText) {
  if (Array.isArray(expectedText)) {
    return expectedText.every((snippet) => actual.includes(snippet));
  }
  return actual === expectedText;
}

export const defaultSampleId = "labeled-tuples-sum-and-product";
export const defaultSample = getSampleById(defaultSampleId);
